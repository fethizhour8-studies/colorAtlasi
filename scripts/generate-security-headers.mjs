import { writeFileSync } from "node:fs";

const adsEnabled = process.env.PUBLIC_ADSENSE_ENABLED === "true";
const plausibleEnabled = Boolean(process.env.PUBLIC_PLAUSIBLE_DOMAIN);
const plausibleOrigin = process.env.PUBLIC_PLAUSIBLE_ORIGIN || "https://plausible.io";

const baseHeaders = [
  "X-Content-Type-Options: nosniff",
  "X-Frame-Options: DENY",
  "Referrer-Policy: strict-origin-when-cross-origin",
  "Permissions-Policy: camera=(), microphone=(), geolocation=(), payment=(), usb=()",
];

function buildCsp({ ads = false } = {}) {
  const scriptSrc = ["script-src", "'self'", "'unsafe-inline'"];
  const connectSrc = ["connect-src", "'self'"];
  const imgSrc = ["img-src", "'self'", "data:"];
  const frameSrc = ["frame-src", "'self'"];

  if (plausibleEnabled) {
    scriptSrc.push(plausibleOrigin);
    connectSrc.push(plausibleOrigin);
  }

  if (ads) {
    imgSrc.push("https:");
    scriptSrc.push(
      "https://pagead2.googlesyndication.com",
      "https://fundingchoicesmessages.google.com",
      "https://www.googletagservices.com",
      "https://securepubads.g.doubleclick.net",
    );
    connectSrc.push(
      "https://pagead2.googlesyndication.com",
      "https://googleads.g.doubleclick.net",
      "https://fundingchoicesmessages.google.com",
      "https://adservice.google.com",
    );
    frameSrc.push(
      "https://googleads.g.doubleclick.net",
      "https://tpc.googlesyndication.com",
      "https://www.google.com",
      "https://fundingchoicesmessages.google.com",
    );
  }

  return [
    "default-src 'self'",
    "base-uri 'self'",
    "object-src 'none'",
    "frame-ancestors 'none'",
    imgSrc.join(" "),
    "font-src 'self' data:",
    "style-src 'self' 'unsafe-inline'",
    scriptSrc.join(" "),
    connectSrc.join(" "),
    frameSrc.join(" "),
    "form-action 'self'",
    "upgrade-insecure-requests",
  ].join("; ");
}

const csp = buildCsp({ ads: adsEnabled });
const profile = adsEnabled ? "AdSense-ready" : "no-ads";
const analyticsProfile = plausibleEnabled ? "with Plausible" : "without analytics";

const lines = [
  "# Generated by scripts/generate-security-headers.mjs.",
  "# Re-run the build after changing PUBLIC_ADSENSE_ENABLED or PUBLIC_PLAUSIBLE_DOMAIN.",
  "/*",
  ...baseHeaders.map((header) => `  ${header}`),
  `  Content-Security-Policy: ${csp}`,
  "",
];

writeFileSync("public/_headers", lines.join("\n"));

const htaccessLines = [
  "# Generated by scripts/generate-security-headers.mjs.",
  "# Re-run the build after changing PUBLIC_ADSENSE_ENABLED or PUBLIC_PLAUSIBLE_DOMAIN.",
  "<IfModule mod_headers.c>",
  ...baseHeaders.map((header) => {
    const [name, ...value] = header.split(": ");
    return `  Header always set ${name} "${value.join(": ").replace(/"/g, '\\"')}"`;
  }),
  `  Header always set Content-Security-Policy "${csp.replace(/"/g, '\\"')}"`,
  "</IfModule>",
  "",
];

writeFileSync("public/.htaccess", htaccessLines.join("\n"));

console.log(`Generated public/_headers and public/.htaccess with ${profile} CSP ${analyticsProfile}.`);
