import { mkdirSync, writeFileSync } from "node:fs";

const plausibleEnabled = Boolean(process.env.PUBLIC_PLAUSIBLE_DOMAIN);
const plausibleOrigin = process.env.PUBLIC_PLAUSIBLE_ORIGIN || "https://plausible.io";

const headers = [
  "X-Content-Type-Options: nosniff",
  "X-Frame-Options: DENY",
  "Referrer-Policy: strict-origin-when-cross-origin",
  "Permissions-Policy: camera=(), microphone=(), geolocation=(), payment=(), usb=()",
];

const scriptSrc = ["script-src", "'self'", "'unsafe-inline'", "https://fonts.googleapis.com"];
const connectSrc = ["connect-src", "'self'"];

if (plausibleEnabled) {
  scriptSrc.push(plausibleOrigin);
  connectSrc.push(plausibleOrigin);
}

const csp = [
  "default-src 'self'",
  "base-uri 'self'",
  "object-src 'none'",
  "frame-ancestors 'none'",
  "img-src 'self' data:",
  "font-src 'self' data: https://fonts.gstatic.com",
  "style-src 'self' 'unsafe-inline' https://fonts.googleapis.com",
  scriptSrc.join(" "),
  connectSrc.join(" "),
  "form-action 'self'",
  "upgrade-insecure-requests",
].join("; ");

mkdirSync("public", { recursive: true });

const headersFile = [
  "# Generated by scripts/generate-security-headers.mjs",
  "/*",
  ...headers.map((header) => `  ${header}`),
  `  Content-Security-Policy: ${csp}`,
  "",
].join("\n");

writeFileSync("public/_headers", headersFile);

const htaccessFile = [
  "# Generated by scripts/generate-security-headers.mjs",
  "<IfModule mod_headers.c>",
  ...headers.map((header) => {
    const [name, ...value] = header.split(": ");
    return `  Header always set ${name} "${value.join(": ").replace(/"/g, '\\"')}"`;
  }),
  `  Header always set Content-Security-Policy "${csp.replace(/"/g, '\\"')}"`,
  "</IfModule>",
  "",
].join("\n");

writeFileSync("public/.htaccess", htaccessFile);

console.log(`Generated static security headers${plausibleEnabled ? " with analytics origin" : ""}.`);
