import { writeFileSync } from "node:fs";

const adsEnabled = process.env.PUBLIC_ADSENSE_ENABLED === "true";

const baseHeaders = [
  "X-Content-Type-Options: nosniff",
  "X-Frame-Options: DENY",
  "Referrer-Policy: strict-origin-when-cross-origin",
  "Permissions-Policy: camera=(), microphone=(), geolocation=(), payment=(), usb=()",
];

const noAdsCsp = [
  "default-src 'self'",
  "base-uri 'self'",
  "object-src 'none'",
  "frame-ancestors 'none'",
  "img-src 'self' data:",
  "font-src 'self' data:",
  "style-src 'self' 'unsafe-inline'",
  "script-src 'self' 'unsafe-inline'",
  "form-action 'self'",
  "upgrade-insecure-requests",
].join("; ");

const adsenseCsp = [
  "default-src 'self'",
  "base-uri 'self'",
  "object-src 'none'",
  "frame-ancestors 'none'",
  "img-src 'self' data: https:",
  "font-src 'self' data:",
  "style-src 'self' 'unsafe-inline'",
  [
    "script-src",
    "'self'",
    "'unsafe-inline'",
    "https://pagead2.googlesyndication.com",
    "https://fundingchoicesmessages.google.com",
    "https://www.googletagservices.com",
    "https://securepubads.g.doubleclick.net",
  ].join(" "),
  [
    "connect-src",
    "'self'",
    "https://pagead2.googlesyndication.com",
    "https://googleads.g.doubleclick.net",
    "https://fundingchoicesmessages.google.com",
    "https://adservice.google.com",
  ].join(" "),
  [
    "frame-src",
    "https://googleads.g.doubleclick.net",
    "https://tpc.googlesyndication.com",
    "https://www.google.com",
    "https://fundingchoicesmessages.google.com",
  ].join(" "),
  "form-action 'self'",
  "upgrade-insecure-requests",
].join("; ");

const lines = [
  "# Generated by scripts/generate-security-headers.mjs.",
  "# Re-run the build after changing PUBLIC_ADSENSE_ENABLED.",
  "/*",
  ...baseHeaders.map((header) => `  ${header}`),
  `  Content-Security-Policy: ${adsEnabled ? adsenseCsp : noAdsCsp}`,
  "",
];

writeFileSync("public/_headers", lines.join("\n"));

const csp = adsEnabled ? adsenseCsp : noAdsCsp;
const htaccessLines = [
  "# Generated by scripts/generate-security-headers.mjs.",
  "# Re-run the build after changing PUBLIC_ADSENSE_ENABLED.",
  "<IfModule mod_headers.c>",
  ...baseHeaders.map((header) => {
    const [name, ...value] = header.split(": ");
    return `  Header always set ${name} "${value.join(": ").replace(/"/g, '\\"')}"`;
  }),
  `  Header always set Content-Security-Policy "${csp.replace(/"/g, '\\"')}"`,
  "</IfModule>",
  "",
];

writeFileSync("public/.htaccess", htaccessLines.join("\n"));

console.log(`Generated public/_headers and public/.htaccess with ${adsEnabled ? "AdSense-ready" : "no-ads"} CSP.`);
